    ...card,
    tradition: 'ponte-d7',
    level: extra.level,
    moduleId: extra.moduleId,
    title: extra.title,
    summary: extra.summary,
    studyNote: extra.studyNote,
    challengePrompt: extra.challengePrompt,
    challengeType: extra.challengeType ?? 'reflection',
    requiredTextMinLength: extra.requiredTextMinLength ?? 20,
    reward: extra.reward,
    linkedSeal: extra.linkedSeal ?? null,
    linkedCard: extra.linkedCard ?? null,
    gameRelation: extra.gameRelation,
    ethicalNote: extra.ethicalNote ?? 'Leitura simbólica e educacional do D7. Não substitui estudo acadêmico ou religioso formal.',
